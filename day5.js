const parsedData = rawData.split('\n').filter(data => !!data)

const cratesClonePart1 = _.cloneDeep(crates)
const cratesClonePart2 = _.cloneDeep(crates)

parsedData.forEach(data => {
    const moveActions = data.split(' ')
    const moveCount = parseInt(moveActions[1])
    const fromIndex = parseInt(moveActions[3]) - 1
    const toIndex = parseInt(moveActions[5]) - 1

    let pushStack = []
    let pushStackReverse = []
    for(let i = 1; i <= moveCount; i++) {
        const firstElementCrate1 = cratesClonePart1[fromIndex].shift()
        const firstElementCrate2 = cratesClonePart2[fromIndex].shift()

        if(firstElementCrate1) pushStack.push(firstElementCrate1)
        if(firstElementCrate2) pushStackReverse.unshift(firstElementCrate2)
    }

    pushStack.forEach(data => cratesClonePart1[toIndex].unshift(data))
    pushStackReverse.forEach(data => cratesClonePart2[toIndex].unshift(data))
})

